

public abstract class Vehicle {
	public Vehicle()
	{
		System.out.println("A vehicle has been selected");
	}
		public void Start()
		{
			System.out.println("A vehicle starts");
		}
		public void Stop()
		{
			System.out.println("A vehicle stops.");
		}
		public void Accelerate()
		{
			System.out.println("A vehicle can accelerate.");
		}
		
	}


